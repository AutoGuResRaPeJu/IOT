          <h2 class="section-heading">Qual o custo?</h2>
        
          <a href="#">
            <img class="img-fluid" src="img/post2.jpg" alt="">
          </a>
          <p>“Tudo depende do grau de integração desejado pelo morador”, explica Muratori. Ele conta que instalações simples de automação custam a partir de R$ 3 mil. “Um indicador normalmente utilizado pelo mercado é estimar o preço de um projeto de automação em algo que varia de 3% a 5% do valor do imóvel para sistemas com maior grau de tecnologia”, conta ele.</p>  

          <p>Outros valores que podem ajudar a nortear o planejamento são trazidos por Plati. “Depende do que o cliente deseja. Para se ter uma boa base, uma cortina automatizada com acionamento por controle remoto começa na faixa de R$1.200. Muitas empresas oferecem pacotes básicos para clientes de primeiro imóvel, que gira em torno de R$ 15 mil e inclui luzes, ar condicionado, cortinas, sistema de som, TV e circuito interno de câmeras”, explica.</p>

