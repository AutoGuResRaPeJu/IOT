        <h2 class="section-heading">O que é automação?</h2> 

        <img class="img-fluid" src="img/post3.jpg" alt=""> 

          <p>As expressões ‘domótica’, ‘casas inteligentes’ e ‘casas conectadas’ também são usadas para se referir ao mesmo conceito. Domótica é a junção do latim domus, que significa casa, com robótica, que se refere ao que é automatizado.</p>

          <p>“É uma nova experiência em moradia, que proporciona conexão e controle da sua casa e dos seus aparelhos eletrônicos, estando você em qualquer lugar, desde um keyboard, ou em um painel, bem como na palma da sua mão, conectado a uma rede wireless. É o resultado da integração de seus aparelhos com comandos automáticos controlados até mesmo por um smartphone ou computador”, explica Plati.</p>

          <p>Em outras palavras, Muratori especifica que “automação residencial é a integração dos diversos sistemas existentes em um lar. A ideia é tornar o controle e utilização da casa mais simples e eficiente”.</p>
          
          <p>A comodidade é o benefício mais evidente, mas não o único. Se aplicada com os cuidados necessários, a automação residencial se torna um importante aliado na segurança da casa.</p>

          <p>“Pensar inicialmente no que você deseja com a automação, não somente como um luxo, mas como algo que, de fato, tornará sua vida mais segura e prática. É bem bacana poder acionar o ar-condicionado à distância e chegar em casa com o apartamento fresquinho, mas investir em um sistema de monitoramento de câmeras e abertura-fechamento de portas pode ser uma ótima pedida pra controlar o acesso à sua casa, bem como ver o que seus filhos ou prestadores de serviço estão fazendo na sua ausência”, pondera o corretor de imóveis.</p> 

          <h2 class="section-heading">O que dá para automatizar?</h2>

          <p>Plati conta que “quase tudo que tem acionamento eletrônico” pode ser automatizado. “Os controles mais comuns e utilizados são cortinas e persianas, câmeras, interfones, aparelhos de ar-condicionado, aquecedores, sistemas de som e luz, banheiras, portas e portões de acesso, sensores de presença, projetores, irrigação e até mesmo alimentador para pets!”, completa.</p>

